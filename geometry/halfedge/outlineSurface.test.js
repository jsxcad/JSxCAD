    [[0.5, -0.5, 0], [-5, -5, 0], [5, -5, 0]],
    [[-5, 5, 0], [-0.5, 0.5, 0], [2.5, 3.5, 0]],
    [[3.5, 2.5, 0], [-0.5, 0.5, 0], [0.5, 0.5, 0]],
    [[0.5, 0.5, 0], [0.5, -0.5, 0], [5, -5, 0]],
    [[5, 5, 0], [-5, 5, 0], [2.5, 3.5, 0]],
    [[3.5, 2.5, 0], [0.5, 0.5, 0], [5, -5, 0]],
    [[5, 5, 0], [2.5, 3.5, 0], [3.5, 3.5, 0]],
    [[3.5, 3.5, 0], [3.5, 2.5, 0], [5, -5, 0]],
    [[5, -5, 0], [5, 5, 0], [3.5, 3.5, 0]]
